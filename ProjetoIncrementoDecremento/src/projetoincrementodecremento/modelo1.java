public class modelo1 {
    public static void main(String[] args) {
        int c;
        c=5;
        System.out.println(c);//5
        System.out.println(c++);//5
        System.out.println(c);//6
        System.out.println();
        c=8;  
        System.out.println(c);//8
        System.out.println(++c);//9
        System.out.println(c);//9
        System.out.println(--c);//8
        System.out.println(c);//8
        System.out.println(c--);//8
        System.out.println(c);//7



    }
}
